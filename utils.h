#include <stdlib.h>
#include <stdint.h>
#include <string.h>

#include <execinfo.h>
#include <signal.h>
#include <unistd.h>

static void handler(int sig) {
  void *array[10];
  size_t size;

  size = backtrace(array, 10);

  fprintf(stderr, "Error: signal %d:\n", sig);
  backtrace_symbols_fd(array, size, STDERR_FILENO);
  exit(1);
}

